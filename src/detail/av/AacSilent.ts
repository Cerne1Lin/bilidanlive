// ── 静音 AAC 帧生成器 ──────────────────────────────────
// 用于音频时间戳间隙填充。从 flv.js src/remux/aac-silent.js 移植。
// 原始出处：dailymotion/hls.js

/**
 * 获取静音 AAC 帧的原始字节
 * @param codec 编解码器字符串，如 'mp4a.40.2' (LC-AAC) 或 'mp4a.40.5' (HE-AAC)
 * @param channelCount 声道数
 * @returns 静音帧字节，不支持时返回 null
 */
export function getSilentFrame(codec: string, channelCount: number): Uint8Array | null {
    if (codec === 'mp4a.40.2') {
        // ── LC-AAC ──────────────────────────────────────
        if (channelCount === 1) {
            return new Uint8Array([0x00, 0xc8, 0x10, 0x80, 0x6d, 0x80])
        } else if (channelCount === 2) {
            return new Uint8Array([0x21, 0x10, 0x04, 0x60, 0x8c, 0x80, 0x02, 0xf8, 0x8c])
        } else if (channelCount === 3) {
            return new Uint8Array([0x01, 0xc8, 0x20, 0xd0, 0x9b, 0x99, 0x00, 0x00, 0x00, 0x1b, 0x80])
        } else if (channelCount === 4) {
            return new Uint8Array([0x01, 0xe0, 0x38, 0x04, 0x00, 0x00, 0x60, 0x00, 0x36, 0x00, 0x01, 0xb4, 0x80])
        } else if (channelCount === 5) {
            return new Uint8Array([0x01, 0xf0, 0x48, 0x08, 0x00, 0x00, 0x60, 0x01, 0x00, 0x01, 0x48, 0x80, 0x01, 0xb4, 0x00, 0x01, 0xb4, 0x80])
        } else if (channelCount === 6) {
            return new Uint8Array([0x01, 0xf8, 0x50, 0x80, 0x0d, 0x18, 0x00, 0x00, 0x60, 0x00, 0x00, 0x46, 0x00, 0x00, 0x46, 0x00, 0x00, 0x46, 0x80])
        }
    } else {
        // ── HE-AAC (SBR/PS) ─────────────────────────────
        if (channelCount === 1) {
            // HE-AAC v1 mono: 59 bytes (含 SBR 扩展)
            return new Uint8Array([
                0x01, 0xa0, 0x22, 0x80, 0xa0, 0x1c, 0x1e, 0x80,
                0x12, 0x08, 0xa0, 0x18, 0x00, 0x6d, 0x01, 0x80,
                0x36, 0x00, 0x06, 0xd0, 0x00, 0x06, 0xd0, 0x00,
                0x16, 0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f,
                0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01,
                0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0,
                0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f,
                0xc0, 0x01, 0x80,
            ])
        } else if (channelCount === 2 || channelCount === 3) {
            // HE-AAC v1 stereo / HE-AAC v2: 58 bytes (含 SBR 扩展)
            return new Uint8Array([
                0x21, 0xa0, 0x22, 0x80, 0xa1, 0x5c, 0x5b, 0x00,
                0x64, 0x87, 0xb8, 0xd8, 0x00, 0x06, 0xd0, 0x01,
                0xb8, 0x00, 0x06, 0xd0, 0x00, 0x16, 0x6f, 0xc0,
                0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f,
                0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01,
                0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0,
                0x01, 0x6f, 0xc0, 0x01, 0x6f, 0xc0, 0x01, 0x6f,
                0xc0, 0x01, 0x80,
            ])
        }
    }
    return null
}
